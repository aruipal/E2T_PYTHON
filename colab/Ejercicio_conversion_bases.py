numero = int(input("Introduce un número: "))

print("El número en binario es:", bin(numero))
print("El número en octal es:", oct(numero))
print("El número en hexadecimal es:", hex(numero), "\n")

print("El número en binario es:", format(numero, "b"))
print("El número en octal es:", format(numero, "o"))
print("El número en hexadecimal es:", format(numero, "x"))